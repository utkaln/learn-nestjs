import { Module } from '@nestjs/common';
import { TasksModule } from './tasks/tasks.module';
import { TypeOrmModule } from '@nestjs/typeorm';

@Module({
  imports: [
    TasksModule,
    TypeOrmModule.forRoot({
      type: 'postgres',
      host: 'localhost',
      port: 5432,
      username: 'postgres',
      password: 'postgres',
      database: 'nest-task-tutorial',
      autoLoadEntities: true, // lets Nest JS automatically find entities
      synchronize: true, // keep db always in sync
    }),
  ],
})
export class AppModule {}
